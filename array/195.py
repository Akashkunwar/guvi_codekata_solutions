# Given a string S, print 'yes' if the strings 'GUVI' and 'GEEK' is present case-sensitively in the string else print 'no'.
# Input Size : 1 <= 100
# 
# 
# Sample Testcase :
# INPUT
# Vishal_Sundar prepared this question
# OUTPUT
# no
# 
# 
# 
# 


# Solution:


a = input()
if ("GUVI" in a) & ("GEEK" in a):
    print("yes")
else:
    print("no")