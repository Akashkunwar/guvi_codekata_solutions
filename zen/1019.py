# QID: 1019
# 
# Given number n and m print the value of n power m
# 
# You will given a number n and m separated by by a space 
# 0<n<100
# 0<m<100
# 
# print the value of n power m
# 
# Sample Input : 
# 3 3
# 
# Sample Output : 
# 27
# 


# Solution:


a,b = input().split()
c = int(a)**int(b)
print(c)