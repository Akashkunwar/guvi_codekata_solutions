# QID: 273
# 
# Given two numbers S1 and S2 represented as strings, return multiplication of the numbers as a string.
# NOTE: The numbers can be arbitrarily large and are nonnegative.
# 
# 
# Sample Testcase :
# INPUT
#  2 10
# OUTPUT
# 20
# 
# 
# 
# 


# Solution:


a,b = map(int,input().split(" "))
print(str(a*b))