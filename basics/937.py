# Write a code to get an integer N and print values from 1 till N in a separate line.
# 
# A single line contains an integer N.
# 
# Print the values from 1 to N in a separate line.
# 
# Sample Input : 
# 5
# 
# Sample Output : 
# 1
# 2
# 3
# 4
# 5
# 


# Solution:


n=int(input())
m = n+1
for x in range(1,m):
  print(x)